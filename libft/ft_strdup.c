#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	size_t	i;
	int		j;

	i = 0;
	j = ft_strlen((char*)s1);
	dup = (char *)malloc(sizeof(*s1) * (j + 1));
	if (dup == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
